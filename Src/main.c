void USART2_IRQHandler(void)
{
	uint8_t u8_Data;
	static uint8_t u8_Count = 0;
	//RX
	if (huart2.Instance->SR & UART_FLAG_RXNE)
	{
		u8_Data = huart2.Instance->DR;
	}

	//TX
	if (huart2.Instance->SR & UART_FLAG_TXE)
	{
		huart2.Instance->DR = 'S';

		u8_Count++;

		if (u8_Count >= 18)
		{
			u8_Count = 18;
			//disable the TRANSFER
			huart2.Instance->CR1 &= ~USART_CR1_TXEIE;
			huart2.Instance->SR &= ~UART_FLAG_TC;
		}
	}
}


void Set_LED_on(uint32_t led_pos)
{
	switch(led_pos){
	case LD4_Pin:
	case LD3_Pin:
	case LD5_Pin:
	case LD6_Pin:
		HAL_GPIO_WritePin(GPIOD, led_pos, GPIO_PIN_SET);

	default:
		;
	}
}

void Set_LED_off(uint32_t led_pos)
{
	switch(led_pos){
	case LD4_Pin:
	case LD3_Pin:
	case LD5_Pin:
	case LD6_Pin:
		HAL_GPIO_WritePin(GPIOD, led_pos, GPIO_PIN_RESET);

	default:
		;
	}
}


